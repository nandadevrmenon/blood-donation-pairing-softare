
public class Person {

	private String name;
	private String bloodGroup;
	private int ID;
	
	public String getName() {
		return this.name;
	}
	
	public void setName(String name) {
		this.name=name;
	}
	
	public String getBlood() {
		return this.bloodGroup;
	}
	
	public void setBloodGroup(String group) {
		bloodGroup=group;
	}
	
	public int getID() {
		return this.ID;
	}
	
	public void setID(int id) {
		this.ID=id;
	}
	


}
